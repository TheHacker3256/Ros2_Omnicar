std::vector<hardware_interface::CommandInterface> DiffBotSystemHardware::export_command_interfaces()
{
  std::vector<hardware_interface::CommandInterface> command_interfaces;

    command_interfaces.emplace_back(hardware_interface::CommandInterface( //declare front left wheel command interface
      flw_.name, hardware_interface::HW_IF_VELOCITY, &flw_.cmd));
  
    command_interfaces.emplace_back(hardware_interface::CommandInterface( //declare front right wheel command interface
      frw_.name, hardware_interface::HW_IF_VELOCITY, &frw_.cmd));

    command_interfaces.emplace_back(hardware_interface::CommandInterface( //declare back left wheel command interface
      blw_.name, hardware_interface::HW_IF_VELOCITY, &blw_.cmd));

    command_interfaces.emplace_back(hardware_interface::CommandInterface( //declare back right wheel command interface
      brw_.name, hardware_interface::HW_IF_VELOCITY, &brw_.cmd));

  return command_interfaces;
}

hardware_interface::CallbackReturn DiffBotSystemHardware::on_activate(
  const rclcpp_lifecycle::State & /*previous_state*/)
{
  //set default values
  flw_.cmd = 0;
  frw_.cmd = 0;
  blw_.cmd = 0;
  brw_.cmd = 0;

  RCLCPP_INFO(get_logger(), "Activating ...please wait...");
  comms_.connect(cfg_.device, cfg_.baud_rate, cfg_.timeout_ms);
  RCLCPP_INFO(get_logger(), "Successfully activated!");
  RCLCPP_INFO(get_logger(), "Connection failed");

  return hardware_interface::CallbackReturn::SUCCESS;
}

hardware_interface::CallbackReturn DiffBotSystemHardware::on_deactivate(
  const rclcpp_lifecycle::State & /*previous_state*/)
{
  RCLCPP_INFO(get_logger(), "Deactivating ...please wait...");

  comms_.disconnect();

  RCLCPP_INFO(get_logger(), "Successfully deactivated!");

  return hardware_interface::CallbackReturn::SUCCESS;
}

hardware_interface::return_type DiffBotSystemHardware::read(
  const rclcpp::Time & /*time*/, const rclcpp::Duration & period)
{

  comms_.set_motor_values(flw_.cmd, frw_.cmd, blw_.cmd, brw_.cmd);

  return hardware_interface::return_type::OK;
}

